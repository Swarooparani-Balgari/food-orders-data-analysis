-----TOTAL AMOUNT------

SELECT
  SUM((ITEMS*ITEMPRICE)+DELIVERYFEE) AS TOTAL_AMOUNT
FROM FOOD_ORDERS;

----TOTAL AMOUNT BY RESTAURANT----

SELECT
  RESTAURANT,
  SUM((ITEMS*ITEMPRICE)+DELIVERYFEE) AS TOTAL_AMOUNT
FROM FOOD_ORDERS
GROUP BY RESTAURANT
ORDER BY TOTAL_AMOUNT DESC;

-----TOTAL AMOUNT BY CITY----

SELECT
  CITY,
  SUM((ITEMS*ITEMPRICE)+DELIVERYFEE) AS TOTAL_AMOUNT
FROM FOOD_ORDERS
GROUP BY CITY
ORDER BY TOTAL_AMOUNT DESC;

-----TOP 3 RESTAURANTS BY TOTAL_AMOUNT-----

SELECT TOP 3
  RESTAURANT,
  SUM((ITEMS*ITEMPRICE)+DELIVERYFEE) AS TOTAL_AMOUNT
FROM FOOD_ORDERS
GROUP BY RESTAURANT
ORDER BY TOTAL_AMOUNT DESC;

----AVERAGE DELIVERY FEE BY CITY----

SELECT
  CITY,
  AVG(DELIVERYFEE) AS AVERAGE_DELIVERYFEE
FROM FOOD_ORDERS
GROUP BY CITY
ORDER BY AVG(DELIVERYFEE) DESC;



